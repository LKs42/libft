#include "libft.h"

int	ft_atoi(const char *str)
{
	long long int i;
	long long int negatif;
	long long int nb;

	negatif = 1;
	i = 0;
	nb = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		negatif = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + ((long long int)str[i] - '0');
		i++;
	}
	return (nb * negatif);
}
