#include "libft.h"

void	ft_putnbr(int a)
{
	unsigned int b;

	b = a;
	if (a < 0)
	{
		ft_putchar('-');
		b = -a;
	}
	if (b >= 10)
	{
		ft_putnbr(b / 10);
		ft_putchar(b % 10 + '0');
	}
	if (b < 10)
		ft_putchar(b % 10 + '0');
}
