char	*ft_strcat(char *dest, char *src)
{
	int i;
	int j;

	j = 0;
	i = -1;
	while (dest[++i])
	{
	}
	while (src[j])
	{
		dest[i] = src[j];
		j++;
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
