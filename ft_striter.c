#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int i;
	int j;

	j = 0;
	i = 0;
	if (s && f)
	{
		while (s[j])
			j++;
		while (i < j)
		{
			f(s);
			s++;
			i++;
		}
	}
}
